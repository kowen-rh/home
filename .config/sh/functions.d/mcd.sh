#!/bin/sh
# =============================================================================
# Function: mcd(dir)
# -----------------------------------------------------------------------------
# Create a directory, then `cd` into it.
# =============================================================================
mcd() {
  if [ $# -eq 1 ] ; then
    mkdir -p "$1" && cd "$1"
  else
    echo 'mcd: give exactly one argument' 1>&2
    return 1
  fi
}
